    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
        // TODO add your handling code here:
        int codigoPedidoVenda = 0, codigoProduto =0;
        double desconto = 0;
        listaModelPedidoVendaProdutos = new ArrayList<>();

        if (jTextFieldDesconto.getText().equals("")) {
            //modelVendas.setVenDesconto(0);
            desconto = 0;

        } else {
            desconto = Double.parseDouble(jTextFieldDesconto.getText());
        }

        if (!jTextFieldNuVenda.getText().equals("")) {
            modelPedidoVendas.setIdvendas(Integer.parseInt(jTextFieldNuVenda.getText()));
        }
        modelPedidoVendas.setFk_cliente(Integer.parseInt(jTextFieldCodCliente.getText()));
        try {
            modelPedidoVendas.setPvDatavenda(datas.converterDataParaDateUS(new java.util.Date(System.currentTimeMillis())));
        } catch (Exception e) {
        }

        modelPedidoVendas.setPvValorliquido(Double.parseDouble(jTextFieldValorTotal.getText()));
        modelPedidoVendas.setPvValorbruto(Double.parseDouble(jTextFieldValorTotal.getText()) + desconto);
        modelPedidoVendas.setPvDesconto(desconto);
        modelPedidoVendas.setPvN_pedido_venda(jTextFieldNPedidoVenda.getText());

        if (SalvarAlterar.equals("salvar")) {

            //salvar vendas
            codigoPedidoVenda = controllerPedidoVendas.salvarPedidoVendaController(modelPedidoVendas);
            if (codigoPedidoVenda > 0) {
                JOptionPane.showMessageDialog(this, "Pedido de venda salvo com Sucesso!", "Informação", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao salvar o pedido da venda!", "Erro", JOptionPane.ERROR_MESSAGE);
            }
            int cont = jTableVendasProdutos.getRowCount();
            for (int i = 0; i < cont; i++) {
                modelPedidoVendasProduto = new ModelPedidoVendasProdutos();
                modelPedidoVendasProduto.setProduto((int) jTableVendasProdutos.getValueAt(i, 0));
                modelPedidoVendasProduto.setVendas(codigoPedidoVenda);
                modelPedidoVendasProduto.setVenProValor((double) jTableVendasProdutos.getValueAt(i, 3));
                modelPedidoVendasProduto.setVenProQuantidade(Integer.parseInt(jTableVendasProdutos.getValueAt(i, 2).toString()));
                listaModelPedidoVendaProdutos.add(modelPedidoVendasProduto);
            }

            //salvar os produtos do pedido da venda
            if (controllerPedidoVendaProduto.salvarVendasProdutosController(listaModelPedidoVendaProdutos)) {
//                JOptionPane.showMessageDialog(this, "Produtos do pedido da venda salvo com Sucesso!", "Informação", JOptionPane.INFORMATION_MESSAGE);
                carregarVendas();
                limparFormulario();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao salvar produtos da venda!", "Erro", JOptionPane.ERROR_MESSAGE);
            }
            jButtonGuardar.setEnabled(true);
        } else {
            //Inicio Alterar
            //Retornar para o estoque e excluir produtos da venda
            int linha = jTableConsuVendaProdutos.getSelectedRow();
            codigoPedidoVenda = (int) jTableConsuVendaProdutos.getValueAt(linha, 0);
            listaModelProdutos = new ArrayList<>();
            listaModelProdutosPedidoVendasProdutos = controllerProdutosPedidoVendasProdutos.getListaProdutosPedidoVendasProdutosController(codigoPedidoVenda);

            if (controllerPedidoVendaProduto.excluirVendasProdutosController(codigoPedidoVenda)) {
                JOptionPane.showMessageDialog(this, "Pedido de venda excluida com sucesso", "Informação", JOptionPane.INFORMATION_MESSAGE);
                carregarVendas();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao excluir o pedido de venda", "Erro", JOptionPane.ERROR_MESSAGE);
            }
            //Fim Alterar

            if (controllerPedidoVendas.atualizarPedidoVendaController(modelPedidoVendas)) {
                JOptionPane.showMessageDialog(this, "Pedido de venda alterada com Sucesso!", "Informação", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao alterar o Pedido da Venda!", "Erro", JOptionPane.ERROR_MESSAGE);
            }
            //adicionar produtos na lista para salvar

            int cont = jTableVendasProdutos.getRowCount();
            for (int i = 0; i < cont; i++) {
                codigoProduto = (int) jTableVendasProdutos.getValueAt(i, 0);
                modelPedidoVendasProduto = new ModelPedidoVendasProdutos();
                modelProdutos = new ModelProdutos();
                
                modelPedidoVendasProduto.setProduto(codigoProduto);
                modelPedidoVendasProduto.setVendas(codigoPedidoVenda);
                modelPedidoVendasProduto.setVenProValor((double) jTableVendasProdutos.getValueAt(i, 3));
                modelPedidoVendasProduto.setVenProQuantidade(Integer.parseInt(jTableVendasProdutos.getValueAt(i, 2).toString()));
                listaModelPedidoVendaProdutos.add(modelPedidoVendasProduto);
            }

            //salvar os produtos do pedido da venda
            if (controllerPedidoVendaProduto.salvarVendasProdutosController(listaModelPedidoVendaProdutos)) {
//                JOptionPane.showMessageDialog(this, "Produtos do pedido da venda salvo com Sucesso!", "Informação", JOptionPane.INFORMATION_MESSAGE);
                carregarVendas();
                limparFormulario();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao salvar produtos da venda!", "Erro", JOptionPane.ERROR_MESSAGE);
            }
            jButtonGuardar.setEnabled(true);
        }

    }//GEN-LAST:event_jButtonGuardarActionPerformed

