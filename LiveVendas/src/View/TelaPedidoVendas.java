    private void jButtonImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonImprimirActionPerformed
        // TODO add your handling code here:

        //método de imprimir 
        if (!jTextFieldNuVenda.getText().equals("")) {
            Map p = new HashMap();
            p.put("idpedidovendas", jTextFieldNuVenda.getText());
            JasperReport report;
            JasperPrint print;
            try {
                report = JasperCompileManager.compileReport(new File("").getAbsolutePath()
                        + "/src/Relatorios/relPedidoVendas.jrxml");
                print = JasperFillManager.fillReport(report, p, connection);
                JasperViewer view = new JasperViewer(print, false);
                view.setIconImage(Toolkit.getDefaultToolkit().getImage(getClass().getResource("/Logos/log.png")));
                view.setTitle("Imprimir Factura Proforma");
                view.setVisible(true);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }

    }//GEN-LAST:event_jButtonImprimirActionPerformed

