    private void somarValorTotalProdutos() {
        double soma = 0, valor;
        int cont = jTableVendasProdutos.getRowCount();
        for (int i = 0; i < cont; i++) {
            valor = (double) jTableVendasProdutos.getValueAt(i, 4);
            soma = soma + valor;

        }
        jTextFieldValorTotal.setText(String.valueOf(soma));
        aplicarDesconto();
    }

    private void aplicarDesconto() {

        try {
            jTextFieldValorTotal.setText(String.valueOf(Double.parseDouble(jTextFieldValorTotal.getText()) - Double.parseDouble(jTextFieldDesconto.getText())));
        } catch (NumberFormatException e) {
        }

    }

    private void listarClientes() {

        listaModelClientes = controllerCliente.getListaClienteController();
        uJComboBoxNCliente.removeAllItems();

        for (int i = 0; i < listaModelClientes.size(); i++) {
            uJComboBoxNCliente.addItem(listaModelClientes.get(i).getCliNome());
        }
    }

    private void listarProdutos() {

        listaModelProdutos = controllerProduto.retornarListaProdutoController();
        uJComboBoxProduto.removeAllItems();

        for (int i = 0; i < listaModelProdutos.size(); i++) {
            uJComboBoxProduto.addItem(listaModelProdutos.get(i).getProNome());
        }
    }

    public void carregarVendas() {
        DefaultTableModel modelo = (DefaultTableModel) jTableConsuVendaProdutos.getModel();
        listaModelVendasCliente = controllerVendasCliente.getListaPedidoVendasClienteController();

        int cont = listaModelVendasCliente.size();
        modelo.setNumRows(0);
        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
                listaModelVendasCliente.get(i).getModelPedidoVendas().getIdvendas(),
                listaModelVendasCliente.get(i).getModelClientes().getCliNome(),
                listaModelVendasCliente.get(i).getModelPedidoVendas().getPvDatavenda()
            });

        }
    }

