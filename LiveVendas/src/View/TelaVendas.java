    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
        // TODO add your handling code here:
        int codigoVenda = 0, codigoProduto = 0;
        double desconto = 0;
        listaModelVendasProdutos = new ArrayList<>();

        if (jTextFieldDesconto.getText().equals("")) {
            //modelVendas.setVenDesconto(0);
            desconto = 0;

        } else {
            desconto = Double.parseDouble(jTextFieldDesconto.getText());
        }
        if (!jTextFieldNuVenda.getText().equals("")) {
            modelVendas.setIdVenda(Integer.parseInt(jTextFieldNuVenda.getText()));
        }
        modelVendas.setCliente(Integer.parseInt(jTextFieldCodCliente.getText()));
        try {
            modelVendas.setVenDataVenda(datas.converterDataParaDateUS(new java.util.Date(System.currentTimeMillis())));
        } catch (Exception e) {
        }
        modelVendas.setVenValorLiquido(Double.parseDouble(jTextFieldValorTotal.getText()));
        modelVendas.setVenValorBruto(Double.parseDouble(jTextFieldValorTotal.getText()) + desconto);
        modelVendas.setVenDesconto(desconto);
        modelVendas.setVenFormaPagamento(jComboBoxTipoPaga.getSelectedItem().toString());
        if (SalvarAlterar.equals("salvar")) {
            //salva

            //salvar vendas
            codigoVenda = controllerVendas.salvarVendasController(modelVendas);
            if (codigoVenda > 0) {

                JOptionPane.showMessageDialog(this, "Venda Salvo com Sucesso!", "Informação", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao salvar a Venda!", "Erro", JOptionPane.ERROR_MESSAGE);
            }

            int cont = jTableVendasProdutos.getRowCount();

            for (int i = 0; i < cont; i++) {
                codigoProduto = (int) jTableVendasProdutos.getValueAt(i, 0);
                modelVendasProdutos = new ModelVendasProdutos();
                modelProdutos = new ModelProdutos();

                modelVendasProdutos.setProduto(codigoProduto);
                modelVendasProdutos.setVendas(codigoVenda);
                modelVendasProdutos.setVenProValor((double) jTableVendasProdutos.getValueAt(i, 3));
                modelVendasProdutos.setVenProQuantidade(Integer.parseInt(jTableVendasProdutos.getValueAt(i, 2).toString()));

                //Produtos
                modelProdutos.setIdProduto(codigoProduto);
                modelProdutos.setProEstoque(controllerProduto.retornaProdutoController(codigoProduto).getProEstoque() - Integer.parseInt(jTableVendasProdutos.getValueAt(i, 2).toString()));
                listaModelVendasProdutos.add(modelVendasProdutos);
                listaModelProdutos.add(modelProdutos);

            }

            //salvar os produtos da venda
            if (controllerVendasProdutos.salvarVendasProdutosController(listaModelVendasProdutos)) {
                //Alterar o estoque dos produtos
                controllerProduto.alterarEstoqueProdutoController(listaModelProdutos);
                //JOptionPane.showMessageDialog(this, "Produtos da venda salvo com Sucesso!", "Informação", JOptionPane.INFORMATION_MESSAGE);
                carregarVendas();
                limparFormulario();
                jButtonGuardar.setEnabled(!true);
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao salvar produtos da venda!", "Erro", JOptionPane.ERROR_MESSAGE);
            }

        } else {
            //altera
            //Retornar para o estoque e excluir produtos da venda
            int linha = jTableConsuVendaProdutos.getSelectedRow();
            codigoVenda = (int) jTableConsuVendaProdutos.getValueAt(linha, 0);
            listaModelProdutos = new ArrayList<>();
            listaModelProdutosVendasProdutos = controllerProdutosVendasProdutos.getListaProdutosVendasProdutosController(codigoVenda);

            for (int i = 0; i < listaModelProdutosVendasProdutos.size(); i++) {
                modelProdutos = new ModelProdutos();

                modelProdutos.setIdProduto(listaModelProdutosVendasProdutos.get(i).getModelProdutos().getIdProduto());
                modelProdutos.setProEstoque(listaModelProdutosVendasProdutos.get(i).getModelProdutos().getProEstoque()
                        + listaModelProdutosVendasProdutos.get(i).getModelVendasProdutos().getVenProQuantidade());
                listaModelProdutos.add(modelProdutos);

            }

            if (controllerProduto.alterarEstoqueProdutoController(listaModelProdutos)) {
                if (controllerVendasProdutos.excluirVendasProdutosController(codigoVenda)) {
                    //JOptionPane.showMessageDialog(this, "Venda excluida com Sucesso!", "Informação", JOptionPane.INFORMATION_MESSAGE);
                    carregarVendas();
                } else {
                    JOptionPane.showMessageDialog(this, "Erro ao excluir a Venda!", "Erro", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao excluir a Venda!", "Erro", JOptionPane.ERROR_MESSAGE);
            }
            //Fim

            if (controllerVendas.atualizarVendasController(modelVendas)) {
                JOptionPane.showMessageDialog(this, "Venda alterada com Sucesso!", "Informação", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao alterar a Venda!", "Erro", JOptionPane.ERROR_MESSAGE);
            }

            //adicionar produtos na lista para salvar
            int cont = jTableVendasProdutos.getRowCount();
            for (int i = 0; i < cont; i++) {
                codigoProduto = (int) jTableVendasProdutos.getValueAt(i, 0);
                modelVendasProdutos = new ModelVendasProdutos();
                modelProdutos = new ModelProdutos();

                modelVendasProdutos.setProduto(codigoProduto);
                modelVendasProdutos.setVendas(codigoVenda);
                modelVendasProdutos.setVenProValor((double) jTableVendasProdutos.getValueAt(i, 3));
                modelVendasProdutos.setVenProQuantidade(Integer.parseInt(jTableVendasProdutos.getValueAt(i, 2).toString()));

                //Produtos
                modelProdutos.setIdProduto(codigoProduto);
                modelProdutos.setProEstoque(controllerProduto.retornaProdutoController(codigoProduto).getProEstoque()
                        - Integer.parseInt(jTableVendasProdutos.getValueAt(i, 2).toString()));
                listaModelVendasProdutos.add(modelVendasProdutos);
                listaModelProdutos.add(modelProdutos);
            }

            //salvar produtos da venda
            if (controllerVendasProdutos.salvarVendasProdutosController(listaModelVendasProdutos)) {
                controllerProduto.alterarEstoqueProdutoController(listaModelProdutos);
                //JOptionPane.showMessageDialog(this, "Produtos da venda salvo com Sucesso!", "Informação", JOptionPane.INFORMATION_MESSAGE);
                carregarVendas();
                limparFormulario();
                jButtonGuardar.setEnabled(!true);
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao salvar produtos da venda!", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_jButtonGuardarActionPerformed

