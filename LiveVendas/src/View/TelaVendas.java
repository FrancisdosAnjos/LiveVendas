    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        // TODO add your handling code here:
        int linha = jTableConsuVendaProdutos.getSelectedRow();
        int codigoVenda = (int) jTableConsuVendaProdutos.getValueAt(linha, 0);
        listaModelProdutos = new ArrayList<>();
        listaModelProdutosVendasProdutos = controllerProdutosVendasProdutos.getListaProdutosVendasProdutosController(codigoVenda);

        for (int i = 0; i < listaModelProdutosVendasProdutos.size(); i++) {
            modelProdutos = new ModelProdutos();

            modelProdutos.setIdProduto(listaModelProdutosVendasProdutos.get(i).getModelProdutos().getIdProduto());
            modelProdutos.setProEstoque(listaModelProdutosVendasProdutos.get(i).getModelProdutos().getProEstoque()
                    + listaModelProdutosVendasProdutos.get(i).getModelVendasProdutos().getVenProQuantidade());
            listaModelProdutos.add(modelProdutos);

        }

        if (controllerProduto.alterarEstoqueProdutoController(listaModelProdutos)) {
            controllerVendasProdutos.excluirVendasProdutosController(codigoVenda);
            if (controllerVendas.excluirVendasController(codigoVenda)) {
                JOptionPane.showMessageDialog(this, "Venda excluida com Sucesso!", "Informação", JOptionPane.INFORMATION_MESSAGE);
                carregarVendas();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao excluir a Venda!", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao excluir a Venda!", "Erro", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_jButtonExcluirActionPerformed

    private void jButtonAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAdicionarActionPerformed
        // TODO add your handling code here:
        double quantidade = 0;
        try {
            if (jTextFieldQuantidade.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Por favor preencher os campos vazios", "Atenção", JOptionPane.WARNING_MESSAGE);
            } else if (modelProdutos.getProEstoque() <= 0) {
                JOptionPane.showMessageDialog(rootPane, "Produto em Falta no Estoque, por favor fazer pedido", "Informação", JOptionPane.WARNING_MESSAGE);
            } else {
                modelProdutos = controllerProduto.retornaProdutoController(Integer.parseInt(jTextFieldCoProduto.getText()));

                DefaultTableModel modelo = (DefaultTableModel) jTableVendasProdutos.getModel();
                int cont = 0;
                quantidade = Double.parseDouble(jTextFieldQuantidade.getText());
                for (int i = 0; i < cont; i++) {
                    modelo.setNumRows(0);
                }
                modelo.addRow(new Object[]{
                    modelProdutos.getIdProduto(),
                    modelProdutos.getProNome(),
                    jTextFieldQuantidade.getText(),
                    modelProdutos.getProValor(),
                    quantidade * modelProdutos.getProValor()
                });
                somarValorTotalProdutos();
            }
        } catch (Exception e) {
        }

    }//GEN-LAST:event_jButtonAdicionarActionPerformed

