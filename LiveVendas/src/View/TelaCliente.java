    public static boolean confirmacao(Component frame, String msg) {
        Object[] opcoes = {"Sim", "Não"};
        int opcao = JOptionPane.showOptionDialog(frame, msg, "Confirmar", JOptionPane.DEFAULT_OPTION, JOptionPane.QUESTION_MESSAGE, null, opcoes, opcoes[0]);

        if (opcao == JOptionPane.YES_NO_CANCEL_OPTION) {
            return true;

        } else {
            return false;

        }
    }

    private void carregarClientes() {

        listaModelClientes = controllerClientes.getListaClienteController();
        DefaultTableModel modelo = (DefaultTableModel) jTableCliente.getModel();
        modelo.setNumRows(0);

        //Inserir os clientes na tabela
        int cont = listaModelClientes.size();
        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
                listaModelClientes.get(i).getIdCliente(),
                listaModelClientes.get(i).getCliNome(),
                listaModelClientes.get(i).getCliTelefone(),
                listaModelClientes.get(i).getCliEmail(),
                listaModelClientes.get(i).getCliNif()

            }
            );

        }
    }

