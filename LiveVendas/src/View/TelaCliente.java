    private void salvarCliente() {

        modelCliente.setCliNome(this.jTextFieldNome.getText());
        modelCliente.setCliEndereco(this.jTextFieldEndereco.getText());
        modelCliente.setCliBairro(this.jTextFieldBairro.getText());
        modelCliente.setCliCidade(this.jTextFieldCidade.getText());
        modelCliente.setCliProvincia((String) this.jComboBoxProvincia.getSelectedItem());
        modelCliente.setCliTelefone(this.jTextFieldTelefone.getText());
        modelCliente.setCliEmail(this.jTextFieldEmail.getText());
        modelCliente.setCliNif(this.jTextFieldNif.getText());

        if (controllerClientes.salvarClienteController(modelCliente) > 0) {
            JOptionPane.showMessageDialog(this, "Cliente cadastrado com sucesso!", "Informação", JOptionPane.INFORMATION_MESSAGE);
            carregarClientes();
            inhabilitar();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar o cliente", "Erro", JOptionPane.ERROR_MESSAGE);
            carregarClientes();
        }
    }

    private void alteraCliente() {
        modelCliente.setCliNome(this.jTextFieldNome.getText());
        modelCliente.setCliEndereco(this.jTextFieldEndereco.getText());
        modelCliente.setCliBairro(this.jTextFieldBairro.getText());
        modelCliente.setCliCidade(this.jTextFieldCidade.getText());
        modelCliente.setCliProvincia((String) this.jComboBoxProvincia.getSelectedItem());
        modelCliente.setCliTelefone(this.jTextFieldTelefone.getText());
        modelCliente.setCliEmail(this.jTextFieldEmail.getText());
        modelCliente.setCliNif(this.jTextFieldNif.getText());

        if (controllerClientes.atualizarClienteController(modelCliente)) {
            JOptionPane.showMessageDialog(this, "Cliente alterado com sucesso!", "Informação", JOptionPane.INFORMATION_MESSAGE);
            carregarClientes();
            inhabilitar();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao alterar o cliente!", "Erro", JOptionPane.ERROR_MESSAGE);
            carregarClientes();
        }
    }

